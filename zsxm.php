	<h4 class="tc">国家注册管理会计师（CCMA）资格认证全国统考简章</h4>
	

	<h5>证书简介：</h5>
	我国自上个世纪80年代引进西方管理会计以来，在理论和实践应用上，都取得了一些成功的经验。但无论从研究的广度和深度上，还是在实践中的应用程度上，同西方发达国家相比都有较大差距，难以适应我国市场经济进一步发展和深化改革的需要。造成这种现状的原因有两大方面：一方面现阶段我国管理会计人员整体管理水平偏低，难以承担管理会计师在企业管理中所应承担的领导者、组织者、顾问、规划与整合专家等多重角色。另一方面国内目前持证会计人员达1500万之多，但高级会计人才不足40万，且以传统的财务会计知识体系为主要专业技能，真正管理会计人才初步缺口已达560万。<br>
	现代企业制度最基本的特征有两条：一是产权关系明晰，企业拥有包括国家在内的出资者投资所形成的全部法人财产权，成为享有民事权利、承担民事责任的法人实体；二是经营目的明确，企业按照市场需求组织生产经营，以提高劳动生产率和经济效益为目的。如何才能做到不断增强企业盈利能力的同时，又不至于削弱偿债能力呢？问题的实质无非是企业的人、财、物等经济资源的合理配置，换句话说，现代企业如何去管好、用好企业的全部资源，这涉及到企业管理的方方面面。管理会计正是适应现代企业建立的要求，而越来越被人们所重视。<br>
	根据《国家中长期人才发展规划纲要（2010-2020年）》总体要求，《会计行业中长期人才发展规划（2010-2020年）》具体指导，为了配合财政部、证监会、银监会、审计署、保监会关于印发《企业内部控制基本规范》（财会【2008】7号）的精神要求，中国企业联合会企业管理岗位培训认证管理办公室在中央财经大学的专业技术支持下，在全国范围内开展“注册管理会计师”培训认证工作。目前，在企业经营管理向科学化和精细化转型的必然趋势下，管理会计师在企业经营发展和决策中发挥着重要作用。在我国管理会计师未受到应有的重视，阻碍了企业管理水平的提高。为此，该项培训将为国家和企业培养熟练掌握并运用管理会计理论和管理技术的，同时符合国内特色的高级管理会计人才。<br>
	<h5>一、组织机构</h5>
	证书颁发单位:&nbsp;中国企业联合会<br>
	国家人力资源和社会保障部中国就业培训技术指导中心<br>
	吉林省招生单位：长春市汉达企业管理咨询中心<br>
	认证信息查询：&nbsp;<a href="http://www.cecedu.com/">www.cecedu.com</a>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.chinaccma.org/">www.chinaccma.org</a><br>
	<h5>二、申报对象</h5>
	企业财务总监、总会计师、注册会计师、高级会计师、财务分析师、风险投资经理、预算分析师、财务策划经理、成本管理经理、成本会计师、CEO、董事会秘书、总助、副总经理、部门经理等管理人员、会计师事务所、管理咨询公司从业人员。<br>
	<h5>三、报名条件</h5>
	（一）基本条件<br>
	1、坚持原则，具有良好的职业道德；<br>
	2、认真执行《会计法》和国家统一的会计制度以及有关财经、财税法律、法规规章制度，无严重违反财经法规与纪律的行为；<br>
	3、履行岗位职责，热爱本职工作；<br>
	4、具备经济类相关专业初级以上职称（含初级职称）。<br>
	（二）具体条件<br>
	报名参加注册管理会计师资格培训与考试的，除具备基本条件外，还应具备下列条件之一：<br>
	1、研究生以上学历，从事企业管理、财务管理工作满一年；<br>
	2、大学本科学历，从事企业管理、财务管理工作满三年；<br>
	3、大专学历，从事企业管理、财务管理工作满四年。<br>
	（三）破格申请<br>
	申报条件不够者，具备十年以上工作经验的企业管理、财务管理人员出具单位证明，可破格申请。<br>
