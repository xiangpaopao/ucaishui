	<h5>项目背景</h5>
	21世纪初多宗震惊世界的危机事件触动了国际社会对“风险与危机管理”主题的广泛关注，亚洲风险与危机管理协会&nbsp;(AARCM)是全球第一个从行业角度推出《企业风险管理人员职业标准》的协会，是以《注册企业风险管理师考试大纲》为考试认证基准，对全球企业首席风险官实施首次系统性考核认证的国际权威组织；拥有500多篇具国际水准的由著名企业首席风险官们撰写的风险管理论文、案例和学术报告。为了配合国务院关于2012年对国资委企业和上市公司A股、H股进行强制性提交风险管理报告通知，国家相关部委如财政部、审计署、证监会、保监会、银监会等，均采用了国际先进的COSO体系进行提交。由于其全面性、有效性和普遍原则，COSO框架受到世界上很多组织的称赞并被接受。<br>
	2005&nbsp;年经中国劳动和社会保障部（现中国人力资源和社会保障部）审核批准AARCM的《注册企业风险管理师(CERM)》项目在中国境内注册发证，授予了该证书项目合法化进入中国，CERM证书在国家官方网站可查询（证书贴有人力资源和社会保障部的认证号码）。中国百家大型国有企业的大多数风险管理负责人已经取得了AARCM颁发的职业资格证书，中国百家上市公司风险管理负责人以及多家著名银行的首席风险官也取得了AARCM颁发的证书。显而易见，如今的AARCM已成为全球当之无愧的企业全面风险管理专业研究的领军组织。<br>
