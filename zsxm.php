          <h3>税务会计师</h3>
          <p>税务会计师是以国家现行税收法律法规为依据，以货币计量为基本形式，运用会计学的基本理论和核算方法，连续、系统、全面地对纳税人应纳税款的形成，计算和缴纳；即税务活动所引起的资金运动进行核算和监督，以及税务统筹管理、税务检查、纳税筹划等一系列与税务相关的税务工作，以保障国家利益和纳税人合法权益的一种企业税务会计专职岗位和专业会计人才。</p>
