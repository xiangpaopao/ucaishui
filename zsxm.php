	<h5>注册信贷分析师简介</h5>
	随着经济全球化和金融国际化趋势深入发展，中国金融业融入世界的金融大潮，推动了我国银行业的快速成长，但与此同时也加剧着金融环境巨变，我国金融业之间及与国外金融机构的竞争将会更加激烈。我国银行业只有适应世界经济和金融业发展变革的新形势，不断提高银行的竞争实力，才能促进我国银行业的繁荣和稳定运行，更好地为国家经济社会服务。&nbsp;众所周知，银行信贷业务是银行的核心业务之一，是商业银行的根本。良好的信贷资产组合质量，不仅关乎商业银行的生存和发展，也决定了存款人的利益乃至一国金融体系的安全。长期以来，我国银行业信贷管理存在着不少制度缺陷，信贷管理中存在一定程度的主观性、盲目性和随意性，这种粗放经营的管理模式，形成了今天银行经营的状况。信贷资产质量长期积重难返，这给银行带来了很大的经营风险和损失。而卓越先进的信贷分析、评审和风险管理技术，以及稳健、谨慎的信贷文化，对于任何商业银行而言，是根本中的根本，也是当前信贷管理工作的迫切要求。美国国际金融专业人才协会International&nbsp;Society&nbsp;of&nbsp;Financial&nbsp;Professionals（ISOFP）在美国登记注册的社团组织,&nbsp;该协会通过合并原美国金融分析师协会和原税务筹划师协会两个专业协会而成，侧重于全球金融专业人士的能力发展。（ISOFP）&nbsp;的使命是为金融行业的专业人士提供高水准、&nbsp;国际一流培训及资格认证。ISOFP继推出私人银行家、注册财富管理师证书后，又推出注册信贷分析师证书。<br>
	为促进中国银行信贷的快速发展，中国金融教育发展基金会引进ISOFP的注册信贷分析师（CCRA）及高级注册信贷分析师（SCCRA）国际资格证书。&nbsp;该证书的推出，是在新的国际金融环境下，运用信贷管理基本机理，广泛吸收国际银行信贷管理与运作中的新规则、新技术、新模式，推出金融理论和实践高度统一，国际和国内高度结合的具有实操性的认证项目。本认证项目具有以下几个特征：第一，内容新颖：项目中信贷业务运作流程、分析技术和方法介绍，都体现当今国际银行业最前沿的信贷管理技术创新和发展趋势，因此，该认证能及时反映当前银行信贷业务的新模式和新动向。第二，实操性强：信贷风险是我国银行最大的风险之一，而信贷风险又主要体现为操作风险，本认证项目强调以风险控制为核心，突出信贷业务的规范化操作，将信贷风险控制的体系、机制、方法和手段贯穿于整个信贷业务。并着重介绍了信贷业务的新规则、操作流程、定价管理、信用分析、项目评估、担保应用，信贷风险管理及贷后管理等具体操作实务，具有很强的实践性和实用性。第三，知识结构系统而全面，主要以信贷分析、管理、风险控制为导向，以信贷业务为轴线，全面阐述信贷业务的运行流程和创新趋势，体系独特全面，内在逻辑性强。<br>
