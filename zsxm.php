          <h3>管理会计师</h3>
          <p>管理会计师在西方发达国家是一个受人尊敬的新兴职业，在现代管理中扮演着决策参与者的重要角色。而在我国，无论是管理会计还是管理会计师，都未受到应有的重视，阻碍了企业管理水平的提高。从发展的眼光看，管理会计的职业化是大势所趋。本文将对我国管理会计职业化的必要性、可行性及实现途径作一些探析。 </p>
          <a  class="fr zsConLK" href="javascript:void(0);">&gt;&gt;&gt;国家注册管理会计师（CCMA）资格认证</a> </div>
