          <h4 class="tc">中国税务会计师（CTAC）资格认证全国统考简章</h4>
          <h5>各相关单位：</h5>
          2003年12月26日根据中共中央、国务院在《关于进一步加强人才工作的决定》中提出“企业经营管理人才重在社会和出资人认可”、“专业技术人才的评价重在社会和业内认可”。《中共中央、国务院关于深化教育改革推进素质教育的决定》指出，“在全社会实行学业证书和职业资格证书并重的制度”。《国务院关于大力推进职业教育改革与发展的决定》指出：要“完善学历证书、培训证书和职业资格证书制度”。<br>
          2010年6月6日，经党中央、国务院批准颁布实施《国家中长期人才发展规划纲要》的同时，财政部也制定了《（2010-2020）会计行业中长期人才发展规划》。《会计人才规划》指出，“要推动实现会计领军人才培养与会计专业技术职称（职务）制度、会计专业学位教育制度的有机衔接”即培养造就规模宏大、结构优化、布局合理、高素质的专业会计人才队伍。而从事企业税务管理的岗位和人才方面却没有相应的专业资格认证制度和体系。所以，开展企业税务管理岗位专业资格认证，全面系统学习税务管理知识，为企业培养既懂会计核算，又懂企业税务风险管理的复合型人才，强化企业在依法纳税，维护企业合法权益，规避企业税务风险，提高企业税务管理水平，增强企业涉税处理和应变能力，税务会计师将起到非常重要的作用，对企业持续健康发展具有重要意义。<br>
